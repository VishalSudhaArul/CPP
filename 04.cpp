//Operators

#include <iostream>
using namespace std;

int main() {

    int a=5;
    int b=6;

    int sum = a+b;
    int dif = a-b;

    float div = float(a) /b; // division operator - integer division
    // Explicit Type conversion
    cout<<"sum : "<<sum<<endl;
    cout<<"dif : "<<dif<<endl;
    cout<<"div :"<<div<<endl;
    return 0;
}

